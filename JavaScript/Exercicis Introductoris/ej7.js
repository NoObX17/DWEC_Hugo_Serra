num1 = prompt("Dime un numero");
num2 = prompt("Dime otro numero");

suma = parseInt(num1) + parseInt(num2);
resta = parseInt(num1) - parseInt(num2);
mult = parseInt(num1) * parseInt(num2);
division = parseInt(num1) / parseInt(num2);

alert ("suma: " + suma + "\nresta: " + resta + "\nmultiplicacion: " + mult + "\ndivision: "+ division);